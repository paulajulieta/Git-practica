public class Calculadora {
    
    int sumar(int a, int b){
        int c = 0;
        c = a +b;
        return c;
    }
    int restar(int a, int b){
        int c = 0;
        c = a -b;
        return c;
    }
    int multiplicar(int a, int b){
        int c = 0;
        c = a *b;
        return c;
    }
    float dividir(float a, float b){
        float c = 0;
        c = a /b;
        return  c;
    }
    
}
